// ─── Generate FEA Configuration (NASTRAN format) ──────────────────
export function generateFEAConfiguration(part, analysis_type = 'static') {
  const { dims = {}, material = 'aluminum_6061' } = part;

  const configurations = {
    static: {
      title: `Static Analysis - ${part.id || 'Part'}`,
      analysis_type: 'STATIC',
      method: 'DIRECT',
      subcase_id: 1,
      load_scale: 1.0,
    },
    modal: {
      title: `Modal Analysis - ${part.id || 'Part'}`,
      analysis_type: 'MODAL',
      method: 'LANCZOS',
      num_modes: 20,
      frequency_range: { start_hz: 0, end_hz: 10000 },
    },
    harmonic: {
      title: `Harmonic Analysis - ${part.id || 'Part'}`,
      analysis_type: 'HARMONIC',
      method: 'DIRECT',
      frequency_range: { start_hz: 10, end_hz: 5000 },
      frequency_points: 200,
      damping_ratio: 0.02,
    },
    thermal: {
      title: `Thermal Analysis - ${part.id || 'Part'}`,
      analysis_type: 'THERMAL',
      method: 'STEADY_STATE',
      boundary_condition: 'convection',
      ambient_temp_c: 25,
      convection_coeff_w_m2k: 25,
    },
  };

  return {
    case_title: configurations[analysis_type]?.title || 'Analysis',
    configuration: configurations[analysis_type] || configurations.static,
    material_id: material,
    mesh_settings: {
      element_type: 'CTETRA',
      max_element_size_mm: Math.min(dims.x, dims.y, dims.z) * 0.1,
      growth_ratio: 1.2,
      boundary_layer_elements: true,
      boundary_layer_size_mm: Math.min(dims.x, dims.y, dims.z) * 0.01,
    },
    output_requests: {
      displacement: true,
      stress: true,
      strain: true,
      principal_stresses: true,
      von_mises: true,
      safety_margin: true,
    },
  };
}

// ─── Generate NASTRAN Bulk Data Format ────────────────────────────
export function generateNASTRANBulkData(part, loads = {}) {
  const { dims = {}, material = 'aluminum_6061', mass_kg = 1 } = part;
  const { force_n = 1000, pressure_mpa = 10, temperature_c = 25 } = loads;

  const nastran_cards = [];

  // Title card
  nastran_cards.push('TITLE = CAD Part - NASTRAN Analysis');
  nastran_cards.push('SUBTITLE = Automatically Generated');
  nastran_cards.push('');

  // Property definition
  nastran_cards.push('$ PROPERTY DEFINITION');
  nastran_cards.push('PSOLID,1,1,');
  nastran_cards.push('');

  // Material definition
  nastran_cards.push('$ MATERIAL PROPERTIES');
  nastran_cards.push(`MAT1,1,69000.,0.33,0.0,2.7,,,${material}`);
  nastran_cards.push('');

  // Boundary conditions
  nastran_cards.push('$ BOUNDARY CONDITIONS');
  nastran_cards.push('SPC1,1,123456,1');
  nastran_cards.push('');

  // Loading
  nastran_cards.push('$ LOADS');
  if (force_n > 0) {
    nastran_cards.push(`FORCE,2,2,0,${force_n}.,0.0,0.0,1.0`);
  }
  if (pressure_mpa > 0) {
    nastran_cards.push(`PLOAD2,3,${pressure_mpa},100,200,300`);
  }
  if (temperature_c !== 25) {
    const temp_delta = temperature_c - 25;
    nastran_cards.push(`TEMP,4,1,${temp_delta}`);
  }
  nastran_cards.push('');

  // Output requests
  nastran_cards.push('$ OUTPUT REQUESTS');
  nastran_cards.push('DISPLACEMENT(PRINT) = ALL');
  nastran_cards.push('STRESS(PRINT,PUNCH) = ALL');
  nastran_cards.push('STRAIN(PRINT) = ALL');
  nastran_cards.push('FORCE(PRINT) = ALL');

  return nastran_cards.join('\n');
}

// ─── Generate ABAQUS Model Definition ────────────────────────────
export function generateABAQUSModelDefinition(part, analysis_type = 'static') {
  const { dims = {}, material = 'aluminum_6061' } = part;

  const abaqus_script = [];

  abaqus_script.push('# ABAQUS Model Definition Script');
  abaqus_script.push(`# Part: ${part.id || 'AutoGenerated'}`);
  abaqus_script.push(`# Analysis Type: ${analysis_type}`);
  abaqus_script.push('');

  abaqus_script.push('from abaqus import *');
  abaqus_script.push('from abaqusConstants import *');
  abaqus_script.push('');

  abaqus_script.push('# Create model');
  abaqus_script.push('mdb.Model(name="CAD_Analysis")');
  abaqus_script.push('model = mdb.models["CAD_Analysis"]');
  abaqus_script.push('');

  abaqus_script.push('# Create part');
  abaqus_script.push('part = model.Part(name="Part_1", dimensionality=THREE_D, type=DEFORMABLE_BODY)');
  abaqus_script.push('');

  abaqus_script.push('# Create material');
  abaqus_script.push(`material = model.Material(name="${material}")`);
  abaqus_script.push('material.Elastic(table=((69000.0, 0.33), ))');
  abaqus_script.push('material.Density(table=((2.7, ), ))');
  abaqus_script.push('');

  abaqus_script.push('# Create section');
  abaqus_script.push(`section = model.SolidSection(name="Section", material="${material}")`);
  abaqus_script.push('part.SectionAssignment(region=part.cells, sectionName="Section")');
  abaqus_script.push('');

  abaqus_script.push('# Create assembly');
  abaqus_script.push('assembly = model.rootAssembly');
  abaqus_script.push('assembly.Instance(name="Part_1-1", part=part, dependent=OFF)');
  abaqus_script.push('');

  if (analysis_type === 'static') {
    abaqus_script.push('# Static analysis step');
    abaqus_script.push('model.StaticStep(name="Static", previous="Initial")');
    abaqus_script.push('model.FieldOutputRequest(step="Static", variables=("S", "E", "U", "CF", "SF"))');
    abaqus_script.push('model.HistoryOutputRequest(step="Static", variables=("TOTALFORCE", "U"))');
  } else if (analysis_type === 'modal') {
    abaqus_script.push('# Modal analysis step');
    abaqus_script.push('model.FrequencyStep(name="Modal", previous="Initial", numEigen=20)');
    abaqus_script.push('model.FieldOutputRequest(step="Modal", variables=("U", "RF"))');
  } else if (analysis_type === 'thermal') {
    abaqus_script.push('# Thermal analysis step');
    abaqus_script.push('model.HeatTransferStep(name="Thermal", previous="Initial")');
    abaqus_script.push('model.FieldOutputRequest(step="Thermal", variables=("NT11", "HFL"))');
  }

  abaqus_script.push('');
  abaqus_script.push('# Job creation');
  abaqus_script.push('mdb.Job(name="CAD_Job", model="CAD_Analysis", numCpus=4)');

  return abaqus_script.join('\n');
}

// ─── Generate Mesh Settings for Different Solvers ─────────────────
export function generateMeshSettings(part, solver_type = 'ansys') {
  const { dims = {} } = part;

  const base_element_size = Math.min(dims.x, dims.y, dims.z) * 0.05;

  const settings = {
    ansys: {
      element_type: 'SOLID187',
      element_size: base_element_size,
      growth_ratio: 1.2,
      curvature_elements: true,
      proximity_elements: false,
      boundary_layer: {
        enabled: true,
        num_layers: 3,
        growth_rate: 1.2,
        first_layer_height: base_element_size * 0.1,
      },
      advanced_meshing: {
        multizone: true,
        local_sizing: true,
        pinch_control: true,
        automatic_transition: true,
      },
      quality_checks: {
        aspect_ratio_limit: 100,
        jacobian_check: true,
        skewness_check: true,
      },
    },

    abaqus: {
      element_type: 'C3D10M',
      global_seed: base_element_size,
      local_seeds: [],
      technique: 'SWEEP',
      allow_mapping: true,
      mesh_controls: {
        quad_dominated: false,
        hex_dominated: true,
      },
      adaptive_meshing: true,
      remesh_on_convergence: true,
    },

    cfd_fluent: {
      element_type: 'TETRA_10',
      base_size: base_element_size * 2,
      min_size: base_element_size * 0.5,
      inflation_layers: 10,
      inflation_ratio: 1.2,
      smoothing: 0.7,
      growth_rate: 1.1,
      max_aspect_ratio: 1000,
    },
  };

  return {
    solver: solver_type,
    mesh_parameters: settings[solver_type] || settings.ansys,
    estimated_element_count: Math.round((dims.x * dims.y * dims.z) / Math.pow(base_element_size, 3) * 1.2),
    estimated_computation_time_minutes: Math.round(((dims.x * dims.y * dims.z) / 1000) * 0.5),
  };
}

// ─── Generate STL with Mesh Refinement ────────────────────────────
export function generateRefinedSTL(part, refinement_level = 2) {
  const { dims = {} } = part;

  // Subdivision levels affect mesh density
  const vertices_per_axis = [50, 100, 200, 400][Math.min(refinement_level, 3)];
  const total_vertices = vertices_per_axis * vertices_per_axis * vertices_per_axis;
  const triangles = total_vertices * 5; // Rough estimate

  return {
    part_id: part.id,
    refinement_level: refinement_level,
    quality_level: ['coarse', 'medium', 'fine', 'very_fine'][Math.min(refinement_level, 3)],
    
    mesh_statistics: {
      total_vertices: total_vertices,
      total_triangles: triangles,
      average_edge_length_mm: Math.min(dims.x, dims.y, dims.z) / vertices_per_axis,
      file_size_mb: Number((triangles * 0.05 / 1000000).toFixed(2)),
    },

    recommended_use: {
      0: 'Visualization only',
      1: '3D printing preview',
      2: 'FEA mesh generation',
      3: 'High-precision simulation',
    }[refinement_level],

    export_formats: ['STL (ASCII)', 'STL (Binary)', 'OBJ', 'PLY'],
  };
}

// ─── Generate Simulation Boundary Conditions ──────────────────────
export function generateBoundaryConditions(part, scenario = 'standard') {
  const scenarios = {
    standard: {
      constraints: [
        { name: 'fixed_base', dof: '123456', regions: ['bottom_face'] },
        { name: 'symmetry_x', dof: '123456', regions: ['symmetry_plane_x'] },
      ],
      loads: [
        { name: 'gravity', type: 'body_force', magnitude: 9.81, direction: [0, -1, 0] },
        { name: 'pressure', type: 'pressure', magnitude: 0.1, regions: ['top_face'] },
      ],
    },

    high_stress: {
      constraints: [
        { name: 'fixed_base', dof: '123456', regions: ['bottom_face'] },
      ],
      loads: [
        { name: 'tension', type: 'force', magnitude: 5000, direction: [0, 1, 0], regions: ['top_face'] },
        { name: 'thermal', type: 'temperature', value: 100, regions: ['all'] },
      ],
    },

    vibration: {
      constraints: [
        { name: 'pinned_base', dof: '123', regions: ['center_hole'] },
      ],
      loads: [
        { name: 'acceleration', type: 'acceleration', magnitude: 10, direction: [0, 1, 0] },
        { name: 'excitation', type: 'force', magnitude: 500, frequency_hz: 60, regions: ['top_face'] },
      ],
    },

    thermal: {
      constraints: [
        { name: 'fixed_edges', dof: '123456', regions: ['perimeter'] },
      ],
      loads: [
        { name: 'heat_flux', type: 'heat_flux', magnitude: 50000, regions: ['surface'] },
        { name: 'convection', type: 'convection', coeff_w_m2k: 100, ambient_c: 25, regions: ['exterior'] },
      ],
    },
  };

  const scenario_data = scenarios[scenario] || scenarios.standard;

  return {
    scenario_name: scenario,
    constraints: scenario_data.constraints,
    loads: scenario_data.loads,
    analysis_type: scenario === 'vibration' ? 'modal_and_harmonic' : scenario === 'thermal' ? 'thermal' : 'static',
    damping: scenario === 'vibration' ? 0.03 : 0.0,
    nonlinear: scenario === 'high_stress' ? true : false,
  };
}

// ─── Generate Solver Configuration File ───────────────────────────
export function generateSolverConfig(analysis_params = {}) {
  const { solver = 'ansys', max_iterations = 1000, convergence_tolerance = 1e-6, time_steps = 10 } = analysis_params;

  const configs = {
    ansys: `
/title, CAD Part - ANSYS Analysis
/prep7
! Mesh
! Loads and Boundary Conditions
! Solution Settings
/solu
solve
finish
/post1
`,

    abaqus: `
# ABAQUS solver configuration
*Step, name=Step1, nlgeom=NO
*Static
${time_steps}, ${time_steps}, ${time_steps * 0.1}, ${time_steps}
*Output, field, variable=PRESELECT
*Output, history, variable=PRESELECT
*End Step
`,

    nastran: `
SOL 101 STATIC ANALYSIS
CEND
TITLE = CAD Part - NASTRAN FEA
SUBTITLE = Auto-Generated
ECHO = UNSORTED
MAXLINES = 999999999
SUBCASE 1
   LOAD = 1
   DISP = ALL
   STRESS = ALL
ENDDATA
`,

    comsol: `
model.study.create('std1');
std1 = model.study('std1');
std1.create('st1', 'Stationary');
std1.feature('st1').setSolverIndex(0);
std1.runAll;
`,
  };

  return {
    solver_type: solver,
    configuration: configs[solver] || configs.ansys,
    convergence_criteria: {
      max_iterations: max_iterations,
      tolerance: convergence_tolerance,
      residual_limit: convergence_tolerance * 10,
    },
    time_integration: {
      total_time_steps: time_steps,
      adaptive_stepping: true,
    },
  };
}

